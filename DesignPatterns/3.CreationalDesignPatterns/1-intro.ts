/*
  Introduction to Creational Design Patterns:

   Creational design patterns in object-oriented programming deal with object creation mechanisms, 
   trying to create objects in a manner suitable to the situation. The basic form of object creation 
   could result in design problems or add complexity to the design. Creational design patterns solve
   this problem by somehow controlling this object creation process.

    There are five creational design patterns:
    1. Singleton
    2. Factory Method
    3. Abstract Factory
    4. Builder
    5. Prototype
    

*/